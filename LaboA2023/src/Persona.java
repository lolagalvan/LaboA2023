public class Persona {
    private String nombre;
    private int edad;
    private String direccion;
}
    public Persona(String nom, int edad, String dir){
        nombre = nom;
        edad = edad;
        direccion = dir;
}
    public void Persona(String nombre, String edad, String direccion) {
        this.nombre = nombre;
        this.edad = edad;
        this.direccion = direccion;
    }
